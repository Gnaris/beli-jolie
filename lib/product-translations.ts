// ─── Translation engine ──────────────────────────────────────────────────

const CACHE_PREFIX = "bj_ptrans_";
const CACHE_VERSION = "v1";

function getCacheKey(locale: string, text: string): string {
  return `${CACHE_PREFIX}${CACHE_VERSION}_${locale}_${text}`;
}

function getCached(locale: string, text: string): string | null {
  if (typeof window === "undefined") return null;
  try {
    return localStorage.getItem(getCacheKey(locale, text));
  } catch {
    return null;
  }
}

function setCache(locale: string, text: string, translation: string): void {
  if (typeof window === "undefined") return;
  try {
    localStorage.setItem(getCacheKey(locale, text), translation);
  } catch {
    // localStorage full — silently ignore
  }
}

/**
 * Translate a French text string using the dictionary.
 * Performs multi-word phrase matching first (longest match wins),
 * then single-word fallback.
 */
function translateWithDict(text: string, dict: Dict): string {
  if (!text) return text;

  let result = text.toLowerCase();

  // Sort dictionary entries by length (longest first) for greedy matching
  const entries = Object.entries(dict).sort(
    (a, b) => b[0].length - a[0].length
  );

  for (const [fr, target] of entries) {
    const regex = new RegExp(`\\b${escapeRegex(fr)}\\b`, "gi");
    result = result.replace(regex, () => target);
  }

  // Capitalize first letter
  if (result.length > 0 && text[0] === text[0].toUpperCase()) {
    result = result[0].toUpperCase() + result.slice(1);
  }

  return result;
}

function escapeRegex(str: string): string {
  return str.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

